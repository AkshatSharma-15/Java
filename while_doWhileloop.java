public class while_doWhileloop {
    public static void main(String[] args) {
        int i=0;
        while(i<5)
        {
            System.out.println("*");
            i++;
        }

        int j=0;
        do
        {
            System.out.println("Do While Loop");
            j++;
        }while(j<5);
    }
}
